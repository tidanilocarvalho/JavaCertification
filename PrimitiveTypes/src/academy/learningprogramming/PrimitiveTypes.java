package academy.learningprogramming;

public class PrimitiveTypes {

    // no size - true|false
    private boolean booleanType;

    // 8-bits - 1 (-128 to +127)
    private byte byteType;

    // 16-bits - 12 (-32768 to +32767)
    private short shortType;

    // 32-bits - 100 (-2147483648 to +2147483647)
    private int intType;

    // 64-bits - 12 (-922337203685477808 to +922337203685477807)
    private long longType;

    // 32-bits - 123.45 (3.40282347E+38)
    private float floatType;

    //64-bits - 123.45 (1.79769313486231570W+308)
    private double doubleType;

    //16-bits - 'a' (0 a 65536)
    private char charType;

}
